//othelloアプリ
$(document).on('mouseenter','#othello',function () {
  $('.fa-delicious').css({ 'color' : '#444' });
  $('.fa-html5').css({ 'color' : '#f06329' });
  $('.fa-css3-alt').css({ 'color' : '#2762ea' });
  $('.fa-js-square').css({ 'color' : '#f5d13a' });
  $('.fa-aws').css({ 'color' : '#f79500' });
  $('.fa-github').css({ 'color' : '#444' });
  $('.circle').css({ 'background-color' : '#c50301' });
})
$(document).on('mouseleave','#othello',function () {
  $('.fa-delicious').css({ 'color' : '' });
  $('.fa-html5').css({ 'color' : '' });
  $('.fa-css3-alt').css({ 'color' : '' });
  $('.fa-js-square').css({ 'color' : '' });
  $('.fa-aws').css({ 'color' : '' });
  $('.fa-github').css({ 'color' : '' });
  $('.circle').css({ 'background-color' : '' });
})

